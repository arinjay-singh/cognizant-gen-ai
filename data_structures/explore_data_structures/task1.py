# Working with Lists

# create a list of fruits
fruits = ["apple", "banana", "blueberry", "raspberry", "strawberry", "watermelon"]
print("Initial list of fruits:", fruits)

# add a fruit to the list (pear)
fruits.append("pear")
print("After adding pear:", fruits)

# remove one fruit form the list with remove() (banana)
fruits.remove("banana")
print("After removing banana:", fruits)

# print list in reverse order using slicing
print("Fruits in reverse order:", fruits[::-1])
